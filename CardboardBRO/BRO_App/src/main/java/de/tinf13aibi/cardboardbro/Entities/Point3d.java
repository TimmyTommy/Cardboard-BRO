public class Point3d {
    public float x;
    public float y;
    public float z;

    public Point3d(){
        x = 0;
        y = 0;
        z = 0;
    }

    public Point3d(float x, float y, float z){
        this.x = x;
        this.y = y;
        this.z = z;
    }
}
